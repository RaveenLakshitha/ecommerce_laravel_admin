function categorizeKey($key)
{
    $keyLower = strtolower($key);
    $categories = [
        'User & Roles Management' => ['user', 'role', 'permission'],
        'Doctors & Staff' => ['doctor', 'staff', 'employee', 'schedule', 'leave', 'attendance', 'hr'],
        'Patients' => ['patient', 'age ', 'blood', 'medical_record'],
        'Appointments' => ['appointment', 'request', 'calendar'],
        'Inventory & Pharmacy' => ['inventory', 'stock', 'medicine', 'pharmacy', 'supplier', 'prescription'],
        'Billing & Finance' => ['billing', 'invoice', 'payment', 'price', 'cost'],
        'Departments & Rooms' => ['department', 'room', 'clinic'],
        'Common / General' => ['dashboard', 'profile', 'name', 'email', 'phone', 'address', 'status', 'action', 'date', 'success', 'error', 'delete', 'edit', 'add', 'search', 'cancel', 'save', 'close'],
    ];
    foreach ($categories as $cat => $keywords) {
        foreach ($keywords as $kw) {
            if (strpos($keyLower, $kw) !== false)
                return $cat;
        }
    }
    return 'Miscellaneous';
}

$grouped = [];
foreach ($translations as $key => $val) {
    $cat = categorizeKey($key);
    $grouped[$cat][$key] = $val;
}

$order = [
    'Common / General',
    'User & Roles Management',
    'Doctors & Staff',
    'Patients',
    'Appointments',
    'Inventory & Pharmacy',
    'Billing & Finance',
    'Departments & Rooms',
    'Miscellaneous'
];

$content = "<?php\n\nreturn [\n";
foreach ($order as $categoryName) {
    if (!empty($grouped[$categoryName])) {
        $content .= "\n    //========================================================\n";
        $content .= "    // $categoryName\n";
        $content .= "    //========================================================\n";
        ksort($grouped[$categoryName]);
        foreach ($grouped[$categoryName] as $key => $val) {
            $escapedKey = addcslashes((string)$key, "'\\");
            $escapedVal = addcslashes((string)$val, "'\\");
            $paddedKey = str_pad("'$escapedKey'", 40);
            $content .= "    $paddedKey => '$escapedVal',\n";
        }
    }
}
$content .= "];\n";
file_put_contents($filePath, $content);

echo "Spanish translations successfully merged and formatted!\n";
